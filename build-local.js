// Script to build TypeScript files for local development
import { exec } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Create a dist directory if it doesn't exist
const distDir = path.join(__dirname, 'dist');
if (!fs.existsSync(distDir)) {
  fs.mkdirSync(distDir);
}

// Compile TypeScript files to JavaScript
console.log('Building TypeScript files for local development...');
exec('npx tsc --skipLibCheck server/**/*.ts --outDir dist', (error, stdout, stderr) => {
  if (error) {
    console.error(`Error: ${error.message}`);
    return;
  }
  if (stderr) {
    console.error(`stderr: ${stderr}`);
    return;
  }
  
  console.log('TypeScript compilation successful!');
  console.log('Now run: npm run local:start');
});